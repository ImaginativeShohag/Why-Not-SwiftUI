import Foundation

func exampleStringToHexToString() {
    /// String to Hex
    /// Here we will process string to generate hex values with commas, so we can use it inside our code. See below code.
    let text = "The Quick Brown Fox Jumps Over The Lazy Dog."
    let textData = Data(text.utf8)
    let hexString = textData.map { String(format: "0x%02x, ", $0) }.joined()
    print(hexString)
    // 0x54, 0x68, 0x65, 0x20, 0x51, 0x75, 0x69, 0x63, 0x6b, 0x20, 0x42, 0x72, 0x6f, 0x77, 0x6e, 0x20, 0x46, 0x6f, 0x78, 0x20, 0x4a, 0x75, 0x6d, 0x70, 0x73, 0x20, 0x4f, 0x76, 0x65, 0x72, 0x20, 0x54, 0x68, 0x65, 0x20, 0x4c, 0x61, 0x7a, 0x79, 0x20, 0x44, 0x6f, 0x67, 0x2e,

    /// Hex array to String
    /// Note: The hex array is created using the above code.
    let hexArray = [
        0x54, 0x68, 0x65, 0x20, 0x51, 0x75, 0x69, 0x63, 0x6b, 0x20, 0x42, 0x72, 0x6f, 0x77, 0x6e, 0x20,
        0x46, 0x6f, 0x78, 0x20, 0x4a, 0x75, 0x6d, 0x70, 0x73, 0x20, 0x4f, 0x76, 0x65, 0x72, 0x20, 0x54,
        0x68, 0x65, 0x20, 0x4c, 0x61, 0x7a, 0x79, 0x20, 0x44, 0x6f, 0x67, 0x2e
    ]
    let resultText = String(hexArray.map { Character(UnicodeScalar($0)!) })
    print(resultText)
    // The Quick Brown Fox Jumps Over The Lazy Dog.
}
